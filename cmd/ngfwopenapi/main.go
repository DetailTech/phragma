// ngfwopenapi applies small hand-authored overlays to the generated Swagger
// document for management HTTP routes that are not backed by protobuf RPCs.
package main

import (
	"bytes"
	"errors"
	"flag"
	"fmt"
	"os"
	"strconv"

	"go.yaml.in/yaml/v3"
)

func main() {
	if err := run(os.Args[1:]); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func run(args []string) error {
	fs := flag.NewFlagSet("ngfwopenapi", flag.ContinueOnError)
	in := fs.String("in", "", "input Swagger YAML")
	out := fs.String("out", "", "output Swagger YAML")
	if err := fs.Parse(args); err != nil {
		return err
	}
	if *in == "" || *out == "" {
		return errors.New("usage: ngfwopenapi --in api-spec.swagger.yaml --out api-spec.swagger.yaml")
	}
	raw, err := os.ReadFile(*in)
	if err != nil {
		return fmt.Errorf("read %s: %w", *in, err)
	}
	overlayed, err := overlay(raw)
	if err != nil {
		return err
	}
	if err := os.WriteFile(*out, overlayed, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", *out, err)
	}
	return nil
}

