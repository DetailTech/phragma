func redirectOperation(operationID, summary, description, failureDescription string, params []*yaml.Node) *yaml.Node {
	pairs := []any{
		"summary", scalar(summary),
		"operationId", scalar(operationID),
		"tags", sequence(scalar("auth")),
		"description", scalar(description),
		"security", sequence(),
	}
	if len(params) > 0 {
		pairs = append(pairs, "parameters", sequence(params...))
	}
	pairs = append(pairs, "responses", mapping(
		"302", mapping("description", scalar("Redirect")),
		"401", mapping("description", scalar(failureDescription)),
	))
	return mapping(pairs...)
}

