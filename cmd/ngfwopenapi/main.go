func genericJSONBody(name string) *yaml.Node {
	return mapping(
		"name", scalar(name),
		"in", scalar("body"),
		"required", boolScalar(true),
		"schema", mapping("type", scalar("object")),
	)
}

func genericJSONBodyRef(name, ref string) *yaml.Node {
	return mapping(
		"name", scalar(name),
		"in", scalar("body"),
		"required", boolScalar(true),
		"schema", refSchema(ref),
	)
}

func complianceOperation(operationID, summary, description string, body, okSchema *yaml.Node, params []*yaml.Node, created, binary bool) *yaml.Node {
	responseNode := mapping("description", scalar("OK"))
	if okSchema != nil {
		responseNode = response("OK", okSchema)
	}
	if binary {
		responseNode = mapping(
			"description", scalar("Report JSON"),
			"headers", mapping(
				"Content-Disposition", typedProperty("string", "Attachment filename for the exported JSON report."),
				"X-Phragma-Payload-Sha256", typedProperty("string", "SHA-256 digest of the exported retained compliance report payload."),
				"ETag", typedProperty("string", "Strong entity tag derived from the exported payload SHA-256 digest."),
			),
			"schema", mapping("type", scalar("file")),
		)
	}
	responses := mapping(
		"200", responseNode,
		"400", response("Validation failed", refSchema("#/definitions/openngfw.v1.ComplianceErrorResponse")),
		"401", response("Unauthenticated", refSchema("#/definitions/openngfw.v1.ComplianceErrorResponse")),
		"403", response("Forbidden", refSchema("#/definitions/openngfw.v1.ComplianceErrorResponse")),
		"404", response("Not found", refSchema("#/definitions/openngfw.v1.ComplianceErrorResponse")),
		"405", response("Method not allowed", refSchema("#/definitions/openngfw.v1.ComplianceErrorResponse")),
		"500", response("Internal error", refSchema("#/definitions/openngfw.v1.ComplianceErrorResponse")),
	)
	if created {
		setMap(responses, "201", mapping(
			"description", scalar("Created"),
			"headers", mapping(
				"Location", typedProperty("string", "Path of the created retained compliance report."),
			),
			"schema", okSchema,
		))
		deleteMap(responses, "200")
	}
	pairs := []any{
		"summary", scalar(summary),
		"operationId", scalar(operationID),
		"tags", sequence(scalar("compliance")),
		"description", scalar(description),
		"responses", responses,
	}
	parameters := append([]*yaml.Node{}, params...)
	if body != nil {
		parameters = append(parameters, body)
	}
	if len(parameters) > 0 {
		pairs = append(pairs, "parameters", sequence(parameters...))
	}
	return mapping(pairs...)
}

